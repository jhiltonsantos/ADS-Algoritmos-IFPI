def main():
    dia = int(input('Dia da semana: '))

    if dia == 1:
        print('1-Domingo')
    elif dia == 2:
        print('2-Segunda')
    elif dia == 3:
        print('3-Terca')
    elif dia == 4:
        print('4-Quarta')
    elif dia == 5:
        print('5-Quinta')
    elif dia == 6:
        print('6-Sexta')
    elif dia == 7:
        print('7-Sabado')
    else:
        print('Valor Invalido')


if __name__ == '__main__':
    main()

